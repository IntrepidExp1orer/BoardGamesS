using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace BoardGamesCore
{
    public class MancalaRules : IGameRules
    {
        public string Name => "Mancala";
        public string Rules => @"Mancala — стратегическая игра для двух игроков.

Игровое поле:
- 2 ряда по 6 лунок
- 2 калаха (дома игроков)

Ход игрока:
- Игрок выбирает одну из своих лунок
- Забирает все камни
- Раскладывает по одму взятому камню в каждую лунку против часовой стрелки
- Свой калах учитывается, калах противника пропускается

Особенности:
- Если последний камень попадает в собственный калах — дополнительный ход
- Если последний камень попадает в пустую лунку игрока и напротив есть камни — происходит захват
- Захваченные камни автоматически перемещаются в калах игрока

Завершение игры:
- Происходит когда у одного игрока пусты все лунки
- Оставшиеся камни переходят в калахи
- Побеждает игрок с наибольшим количеством камней в калахе";
    }
}
